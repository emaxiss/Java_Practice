package foundation;

public class Addition
{
	public int add(int a, int b)
	{
		return a + b;
	}
	
	public int add(int a, int b, int c)
	{
		return a + b + c;
	}
	
	public double add(double a, double b)
	{
		return a + b;
	}
	
// method overloading - to use same name for methods (parameters must be different in count or type), return type doesn't matter.
}
